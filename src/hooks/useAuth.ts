'use client'

import { useState, useEffect, useCallback } from 'react'
import { User } from '@supabase/supabase-js'
import { supabase } from '@/lib/supabase'

interface UserProfile {
  id: string
  email: string
  subscription_plan: 'free' | 'plus' | 'pro' | 'premium' | 'super'
  subscription_status: string
  credits_remaining: number
  updated_at?: string
  force_refresh?: number
}

export function useAuth() {
  const [user, setUser] = useState<UserProfile | null>(null)
  const [loading, setLoading] = useState(true)
  const [isRefreshing, setIsRefreshing] = useState(false)
  const [lastMetadataHash, setLastMetadataHash] = useState<string>('')

  // Fun√ß√£o para gerar hash dos metadados para detectar mudan√ßas
  const generateMetadataHash = useCallback((metadata: any) => {
    const relevantData = {
      subscription_plan: metadata?.subscription_plan,
      credits_remaining: metadata?.credits_remaining,
      updated_at: metadata?.updated_at,
      force_refresh: metadata?.force_refresh
    }
    return JSON.stringify(relevantData)
  }, [])

  const refreshUser = useCallback(async (forceRefresh = false) => {
    try {
      if (isRefreshing && !forceRefresh) {
        console.log('‚è≥ [AUTH] Refresh j√° em andamento, ignorando...')
        return
      }

      setIsRefreshing(true)
      console.log('üîÑ [AUTH] Atualizando dados do usu√°rio...')
      
      const { data: { user: authUser }, error: authError } = await supabase.auth.getUser()
      
      if (authError) {
        console.error('‚ùå [AUTH] Erro ao buscar usu√°rio:', authError)
        setUser(null)
        setLoading(false)
        return
      }

      if (!authUser) {
        console.log('‚ö†Ô∏è [AUTH] Usu√°rio n√£o autenticado')
        setUser(null)
        setLoading(false)
        return
      }

      // Verificar se houve mudan√ßas nos metadados
      const newMetadataHash = generateMetadataHash(authUser.user_metadata)
      const hasChanges = newMetadataHash !== lastMetadataHash || forceRefresh

      if (hasChanges || !user) {
        console.log('üîÑ [AUTH] Detectadas mudan√ßas nos metadados ou primeira carga, atualizando...')
        setLastMetadataHash(newMetadataHash)

        const userProfile: UserProfile = {
          id: authUser.id,
          email: authUser.email || '',
          subscription_plan: authUser.user_metadata?.subscription_plan || 'free',
          subscription_status: authUser.user_metadata?.subscription_status || 'inactive',
          credits_remaining: authUser.user_metadata?.credits_remaining ?? 10,
          updated_at: authUser.user_metadata?.updated_at,
          force_refresh: authUser.user_metadata?.force_refresh
        }

        console.log('‚úÖ [AUTH] Usu√°rio atualizado:', {
          email: userProfile.email,
          plan: userProfile.subscription_plan,
          credits: userProfile.credits_remaining === -1 ? 'ilimitados' : userProfile.credits_remaining,
          updated_at: userProfile.updated_at
        })

        setUser(userProfile)
        
        // Mostrar notifica√ß√£o de atualiza√ß√£o se houve mudan√ßa de plano
        if (user && user.subscription_plan !== userProfile.subscription_plan) {
          console.log(`üéâ [AUTH] Plano atualizado: ${user.subscription_plan} ‚Üí ${userProfile.subscription_plan}`)
          
          // Mostrar notifica√ß√£o no browser se poss√≠vel
          if ('Notification' in window && Notification.permission === 'granted') {
            new Notification('Plano Atualizado!', {
              body: `Seu plano foi atualizado para ${userProfile.subscription_plan}`,
              icon: '/favicon.ico'
            })
          }
        }
      } else {
        console.log('‚ÑπÔ∏è [AUTH] Nenhuma mudan√ßa detectada nos metadados')
      }
      
      setLoading(false)
    } catch (error) {
      console.error('‚ùå [AUTH] Erro ao atualizar usu√°rio:', error)
      setLoading(false)
    } finally {
      setIsRefreshing(false)
    }
  }, [lastMetadataHash, generateMetadataHash, user, isRefreshing])

  // Inicializa√ß√£o e listener de auth
  useEffect(() => {
    console.log('üöÄ [AUTH] Inicializando useAuth...')
    
    let mounted = true
    
    // Fun√ß√£o para carregar usu√°rio inicial
    const loadInitialUser = async () => {
      try {
        console.log('üîÑ [AUTH] Carregando usu√°rio inicial...')
        
        const { data: { user: authUser }, error: authError } = await supabase.auth.getUser()
        
        if (!mounted) return
        
        if (authError) {
          console.error('‚ùå [AUTH] Erro ao buscar usu√°rio:', authError)
          setUser(null)
          setLoading(false)
          return
        }

        if (!authUser) {
          console.log('‚ö†Ô∏è [AUTH] Usu√°rio n√£o autenticado')
          setUser(null)
          setLoading(false)
          return
        }

        console.log('‚úÖ [AUTH] Usu√°rio encontrado:', authUser.email)
        
        // Inicializar hash dos metadados
        const initialHash = generateMetadataHash(authUser.user_metadata)
        setLastMetadataHash(initialHash)
        
        const userProfile: UserProfile = {
          id: authUser.id,
          email: authUser.email || '',
          subscription_plan: authUser.user_metadata?.subscription_plan || 'free',
          subscription_status: authUser.user_metadata?.subscription_status || 'inactive',
          credits_remaining: authUser.user_metadata?.credits_remaining ?? 10,
          updated_at: authUser.user_metadata?.updated_at,
          force_refresh: authUser.user_metadata?.force_refresh
        }
        
        setUser(userProfile)
        setLoading(false)
        
        // Solicitar permiss√£o para notifica√ß√µes
        if ('Notification' in window && Notification.permission === 'default') {
          Notification.requestPermission().then(permission => {
            console.log(`üîî [AUTH] Permiss√£o de notifica√ß√£o: ${permission}`)
          })
        }
        
      } catch (error) {
        console.error('‚ùå [AUTH] Erro inesperado:', error)
        if (mounted) {
          setUser(null)
          setLoading(false)
        }
      }
    }

    // Carregar usu√°rio inicial
    loadInitialUser()

    // Listener para mudan√ßas de auth
    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        console.log('üîÑ [AUTH] Mudan√ßa de estado:', event, session?.user?.email || 'sem usu√°rio')
        
        if (!mounted) return
        
        if (event === 'SIGNED_IN' && session?.user) {
          console.log('‚úÖ [AUTH] Login detectado:', session.user.email)
          await refreshUser(true)
          
        } else if (event === 'SIGNED_OUT') {
          console.log('üëã [AUTH] Logout detectado')
          setUser(null)
          setLoading(false)
          setLastMetadataHash('')
          
        } else if (event === 'TOKEN_REFRESHED' && session?.user) {
          console.log('üîÑ [AUTH] Token atualizado automaticamente:', session.user.email)
          await refreshUser()
          
        } else {
          // Para qualquer outro evento, garantir que loading seja false
          setLoading(false)
        }
      }
    )

    return () => {
      mounted = false
      subscription.unsubscribe()
    }
  }, [refreshUser, generateMetadataHash])

  // Polling inteligente apenas ap√≥s pagamentos (detectado por mudan√ßas espec√≠ficas)
  useEffect(() => {
    if (!user) return

    let checkInterval: NodeJS.Timeout | null = null
    let timeoutId: NodeJS.Timeout | null = null

    // Verificar se h√° indica√ß√£o de pagamento recente
    const hasRecentPayment = user.force_refresh && 
      user.force_refresh > (Date.now() - 120000) // √öltimos 2 minutos

    if (hasRecentPayment) {
      console.log('üí≥ [AUTH] Pagamento recente detectado, iniciando verifica√ß√£o autom√°tica...')
      
      let attempts = 0
      const maxAttempts = 30 // 30 tentativas = 1 minuto
      
      checkInterval = setInterval(async () => {
        attempts++
        console.log(`üîç [AUTH] Verifica√ß√£o autom√°tica ${attempts}/${maxAttempts}...`)
        
        try {
          const { data: { user: authUser } } = await supabase.auth.getUser()
          if (authUser) {
            const newHash = generateMetadataHash(authUser.user_metadata)
            if (newHash !== lastMetadataHash) {
              console.log('‚úÖ [AUTH] Mudan√ßa detectada via verifica√ß√£o autom√°tica!')
              await refreshUser(true)
              if (checkInterval) clearInterval(checkInterval)
              return
            }
          }
        } catch (error) {
          console.error('‚ùå [AUTH] Erro na verifica√ß√£o autom√°tica:', error)
        }
        
        if (attempts >= maxAttempts) {
          console.log('‚è∞ [AUTH] Verifica√ß√£o autom√°tica finalizada')
          if (checkInterval) clearInterval(checkInterval)
        }
      }, 2000) // A cada 2 segundos por 1 minuto
      
      // Timeout de seguran√ßa
      timeoutId = setTimeout(() => {
        if (checkInterval) clearInterval(checkInterval)
        console.log('‚è∞ [AUTH] Timeout da verifica√ß√£o autom√°tica')
      }, 120000) // 2 minutos m√°ximo
    }

    return () => {
      if (checkInterval) clearInterval(checkInterval)
      if (timeoutId) clearTimeout(timeoutId)
    }
  }, [user?.force_refresh, lastMetadataHash, refreshUser, generateMetadataHash])

  const signIn = async (email: string, password: string) => {
    try {
      setLoading(true)
      console.log(`üîê [AUTH] Fazendo login: ${email}`)
      
      const { data, error } = await supabase.auth.signInWithPassword({
        email,
        password,
      })

      if (error) {
        console.error('‚ùå [AUTH] Erro no login:', error)
        throw error
      }

      console.log('‚úÖ [AUTH] Login realizado com sucesso')
      return data
    } catch (error) {
      console.error('‚ùå [AUTH] Erro no signIn:', error)
      throw error
    } finally {
      setLoading(false)
    }
  }

  const signUp = async (email: string, password: string, fullName?: string) => {
    try {
      setLoading(true)
      console.log(`üìù [AUTH] Criando conta: ${email}`)
      
      const { data, error } = await supabase.auth.signUp({
        email,
        password,
        options: {
          data: {
            full_name: fullName || email.split('@')[0],
            subscription_plan: 'free',
            subscription_status: 'active',
            credits_remaining: 10,
            created_at: new Date().toISOString()
          }
        }
      })

      if (error) {
        console.error('‚ùå [AUTH] Erro no cadastro:', error)
        throw error
      }

      console.log('‚úÖ [AUTH] Cadastro realizado com sucesso')
      return data
    } catch (error) {
      console.error('‚ùå [AUTH] Erro no signUp:', error)
      throw error
    } finally {
      setLoading(false)
    }
  }

  const signOut = async () => {
    try {
      setLoading(true)
      console.log('üëã [AUTH] Fazendo logout...')
      
      const { error } = await supabase.auth.signOut()
      
      if (error) {
        console.error('‚ùå [AUTH] Erro no logout:', error)
        throw error
      }

      console.log('‚úÖ [AUTH] Logout realizado com sucesso')
      setUser(null)
      setLastMetadataHash('')
    } catch (error) {
      console.error('‚ùå [AUTH] Erro no signOut:', error)
      throw error
    } finally {
      setLoading(false)
    }
  }

  // Fun√ß√£o para for√ßar refresh manual (para usar ap√≥s pagamentos)
  const forceRefresh = useCallback(async () => {
    console.log('üîÑ [AUTH] Refresh manual solicitado...')
    await refreshUser(true)
  }, [refreshUser])

  return {
    user,
    loading,
    isRefreshing,
    signIn,
    signUp,
    signOut,
    refreshUser: forceRefresh
  }
} 