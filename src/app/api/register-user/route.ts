import { NextRequest, NextResponse } from 'next/server'
import { supabase } from '@/lib/supabase'

export async function POST(request: NextRequest) {
  try {
    console.log('üîê [REGISTER] Iniciando processo de registro...')
    
    const { email, password, fullName } = await request.json()
    console.log(`üîê [REGISTER] Dados recebidos - Email: ${email}, FullName: ${fullName}`)

    if (!email || !password) {
      console.log('‚ùå [REGISTER] Email ou senha n√£o fornecidos')
      return NextResponse.json({ 
        success: false, 
        error: 'Email e senha s√£o obrigat√≥rios' 
      }, { status: 400 })
    }

    if (password.length < 6) {
      console.log('‚ùå [REGISTER] Senha muito curta')
      return NextResponse.json({ 
        success: false, 
        error: 'A senha deve ter pelo menos 6 caracteres' 
      }, { status: 400 })
    }

    // ‚ú® PRIMEIRA TENTATIVA: SUPABASE AUTH (como no app mobile)
    console.log(`üìù [REGISTER] Tentativa 1: Supabase Auth para ${email}`)
    console.log(`üîß [REGISTER] Supabase URL: ${process.env.NEXT_PUBLIC_SUPABASE_URL}`)
    console.log(`üîß [REGISTER] Supabase Key configurada: ${!!process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY}`)
    
    try {
      const { data, error } = await supabase.auth.signUp({
        email: email.trim(),
        password: password,
        options: {
          data: {
            full_name: fullName || email.split('@')[0]
          }
        }
      })

      console.log(`üîç [REGISTER] Resultado do signUp - Data: ${!!data}, Error: ${!!error}`)
      
      if (!error && data.user) {
        console.log('‚úÖ [REGISTER] Usu√°rio criado com sucesso via Supabase Auth! ID:', data.user.id)
        console.log('üìã [REGISTER] Dados do usu√°rio:', {
          id: data.user.id,
          email: data.user.email,
          email_confirmed: !!data.user.email_confirmed_at
        })
        
        // O perfil ser√° criado automaticamente pelo trigger
        // Aguardar um pouco para garantir que o trigger executou
        console.log('‚è≥ [REGISTER] Aguardando trigger de cria√ß√£o de perfil...')
        await new Promise(resolve => setTimeout(resolve, 1000))
        
        return NextResponse.json({ 
          success: true, 
          message: data.user.email_confirmed_at 
            ? 'Conta criada com sucesso! Voc√™ j√° pode fazer login.'
            : 'Conta criada! Verifique seu email para confirmar.',
          user: {
            id: data.user.id,
            email: data.user.email,
            full_name: data.user.user_metadata?.full_name,
            email_confirmed: !!data.user.email_confirmed_at,
            needs_confirmation: !data.user.email_confirmed_at
          },
          method: 'supabase_auth'
        })
      }

      // Se chegou aqui, houve erro na primeira tentativa
      console.warn('‚ö†Ô∏è [REGISTER] Supabase Auth falhou:', error?.message)
      
      // Tratar erros espec√≠ficos que devem retornar imediatamente
      if (error?.message?.includes('already registered') || error?.message?.includes('already been registered')) {
        return NextResponse.json({ 
          success: false, 
          error: 'Este email j√° est√° em uso' 
        }, { status: 400 })
      }
      
    } catch (authError) {
      console.warn('‚ö†Ô∏è [REGISTER] Erro na chamada do Supabase Auth:', authError)
    }

    // ‚ú® SEGUNDA TENTATIVA: FUN√á√ÉO DO BANCO DE DADOS
    console.log(`üîß [REGISTER] Tentativa 2: Fun√ß√£o do banco para ${email}`)
    
    try {
      const { data: dbResult, error: dbError } = await supabase
        .rpc('create_user_with_profile', {
          user_email: email.trim(),
          user_password: password,
          user_full_name: fullName || email.split('@')[0]
        })

      if (dbError) {
        console.error('‚ùå [REGISTER] Erro na fun√ß√£o do banco:', dbError)
        throw dbError
      }

      console.log('üîß [REGISTER] Resultado da fun√ß√£o do banco:', dbResult)

      if (dbResult?.success) {
        console.log('‚úÖ [REGISTER] Usu√°rio criado com sucesso via fun√ß√£o do banco!')
        return NextResponse.json({
          ...dbResult,
          method: 'database_function'
        })
      } else {
        console.error('‚ùå [REGISTER] Fun√ß√£o do banco retornou erro:', dbResult?.error)
        
        // Se for erro de email j√° em uso, retornar erro espec√≠fico
        if (dbResult?.code === 'user_already_exists') {
          return NextResponse.json({ 
            success: false, 
            error: 'Este email j√° est√° em uso' 
          }, { status: 400 })
        }
        
        throw new Error(dbResult?.error || 'Erro na fun√ß√£o do banco')
      }
      
    } catch (dbError) {
      console.error('‚ùå [REGISTER] Erro na fun√ß√£o do banco:', dbError)
    }

    // ‚ú® Se chegou aqui, todas as tentativas falharam
    console.error('‚ùå [REGISTER] Todas as tentativas falharam para:', email)
    return NextResponse.json({ 
      success: false, 
      error: 'Erro ao criar conta. O servi√ßo est√° temporariamente indispon√≠vel. Tente novamente em alguns minutos.',
      details: 'Falha em todos os m√©todos de registro',
      debug: {
        supabase_url: process.env.NEXT_PUBLIC_SUPABASE_URL,
        has_anon_key: !!process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY
      }
    }, { status: 500 })

  } catch (error) {
    console.error('‚ùå [REGISTER] Erro inesperado geral:', error)
    return NextResponse.json({ 
      success: false, 
      error: 'Erro interno do servidor',
      details: error instanceof Error ? error.message : 'Erro desconhecido',
      debug: {
        error_type: typeof error,
        error_name: error instanceof Error ? error.name : 'unknown'
      }
    }, { status: 500 })
  }
}

export async function GET() {
  try {
    // Verificar estat√≠sticas dos perfis
    const { data: stats, error } = await supabase
      .from('profiles')
      .select('subscription_plan, subscription_status')

    const planCount = stats?.reduce((acc, item) => {
      acc[item.subscription_plan] = (acc[item.subscription_plan] || 0) + 1
      return acc
    }, {} as Record<string, number>) || {}

    const statusCount = stats?.reduce((acc, item) => {
      acc[item.subscription_status] = (acc[item.subscription_status] || 0) + 1
      return acc
    }, {} as Record<string, number>) || {}

    return NextResponse.json({ 
      message: 'API de registro de usu√°rio',
      status: 'ativa',
      methods: [
        'Supabase Auth + Profiles (Prim√°rio)',
        'Fun√ß√£o do Banco de Dados (Fallback)'
      ],
      note: 'Sistema robusto com m√∫ltiplas tentativas seguindo padr√£o do app mobile',
      stats: {
        total_profiles: stats?.length || 0,
        by_plan: planCount,
        by_status: statusCount
      },
      debug: {
        supabase_url: process.env.NEXT_PUBLIC_SUPABASE_URL,
        has_anon_key: !!process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY,
        environment: process.env.NODE_ENV
      }
    })
  } catch (error) {
    return NextResponse.json({ 
      message: 'API de registro de usu√°rio',
      status: 'ativa',
      error: 'Erro ao buscar estat√≠sticas',
      debug: {
        error_message: error instanceof Error ? error.message : 'unknown'
      }
    })
  }
} 